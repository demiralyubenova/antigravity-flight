
export const removeBackground = async (file: File, type: 'CLOTHING' | 'PERSON' = 'CLOTHING'): Promise<string> => {
    try {
        const formData = new FormData();
        formData.append('file', file);
        formData.append('type', type);

        const backendUrl = import.meta.env.VITE_BACKEND_URL || 'http://localhost:3011';
        const response = await fetch(`${backendUrl}/api/remove-background`, {
            method: 'POST',
            body: formData,
        });

        if (!response.ok) {
            // Read the actual error from the backend JSON body
            let errorDetail = response.statusText;
            try {
                const errBody = await response.json();
                errorDetail = errBody.error || errorDetail;
            } catch (_) {}
            console.error('Backend remove-background error:', errorDetail);
            throw new Error(`Backend error: ${errorDetail}`);
        }

        const data = await response.json();
        if (data.result) {
            return data.result; // This is a base64 string
        }

        throw new Error("No image generated by background removal");
    } catch (error) {
        console.error("Background removal failed", error);
        throw error;
    }
};
